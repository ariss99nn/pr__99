// Usamos los valores booleanos en JavaScript
let a = true;
let b = false;
console.log(a && b);  // Operador lógico "AND"

// Definimos más variables numéricas
a = 2;
b = 3;
c = 4;
d = 10;

// Usamos los operadores lógicos para comparaciones
console.log(a === b && c < d);  // Operador "AND" con comparación

console.log(!(a === b) && c < d);  // Operador "NOT" con comparación

