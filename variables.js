let a = 10;
let b = 4;

// Multiplicación
console.log('La primera variable es:', a);
console.log('El signo de la operación es: *');
console.log('La segunda variable es:', b);
let c = a * b;
console.log('El resultado es:', c);
console.log('Tipo de resultado:', typeof c);

// División
console.log('La primera variable es:', a);
console.log('El signo de la operación es: /');
console.log('La segunda variable es:', b);
c = a / b;
console.log('El resultado es:', c);
console.log('Tipo de resultado:', typeof c);

// Suma
console.log('La primera variable es:', a);
console.log('El signo de la operación es: +');
console.log('La segunda variable es:', b);
c = a + b;
console.log('El resultado es:', c);
console.log('Tipo de resultado:', typeof c);

// Resta
console.log('La primera variable es:', a);
console.log('El signo de la operación es: -');
console.log('La segunda variable es:', b);
c = a - b;
console.log('El resultado es:', c);
console.log('Tipo de resultado:', typeof c);

// Potencia
console.log('La primera variable es:', a);
console.log('El signo de la operación es: **');
console.log('La segunda variable es:', b);
c = Math.pow(a, b);  // En JavaScript, se usa Math.pow para la potencia
console.log('El resultado es:', c);
console.log('Tipo de resultado:', typeof c);

// Módulo (resto de la división)
console.log('La primera variable es:', a);
console.log('El signo de la operación es: %');
console.log('La segunda variable es:', b);
c = a % b;
console.log('El resultado es:', c);
console.log('Tipo de resultado:', typeof c);